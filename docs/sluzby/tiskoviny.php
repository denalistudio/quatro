                        služeb.
                        Pro zvýšení efektivity reklamního sdělení je nutno co nejpřesněji určit zájmovou skupinu a
                        spádovou
                        oblast v níž mají být reklamní letáky distribuovány. K tomuto účelu můžete využít našich
                        poradenských
                        služeb.</p>
