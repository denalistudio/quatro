                    <p>Vyrábíme, montujeme a instalujeme světelné reklamy:</p>
                    <ul>
                        <li>krabicová jednostranná prosvětlená reklama je nejlevnější varianta světelné reklamy. Skládá
                            se z
                            podkladového materiálu, hliníkových nebo plastových profilů a předního panelu z opálového
                            plexiskla,
                            na které se lepí transluscentní fólie. Tyto fólie dokonale spolu s opálem dokáží rozptýlit
                            světlo.
                            Jako světelné zdroje používáme zářivkové trubice, které mají dobrý světelný výkon, snadnou
                            údržbu,
                            levný provoz a nízké pořizovací náklady.</li>
                        <li>krabicová oboustranná prosvětlená reklama má stejné vlastnosti jako krabicová jednostranná
                            prosvětlená reklama, mimo podkladového materiálu. Reklama je opatřena z obou stran opálovým
                            plexisklem. Výroba je náročnější pro speciální konzoly a vnitřní konstrukci, ale odměnou je
                            dvakrát
                            větší plocha reklamy a to jen s jedním světelným zdrojem.</li>
                        <li>krabicové písmo je jedním z nejpůsobivějším prodstředků reklamy. Prosvětlené nápisy jsou
                            vždy
                            nejúčinnější formou reklamy. Jako světelné zdroje mohou být použity zářivkové trubice,
                            neony, LED
                            moduly nebo minižárovky.</li>
